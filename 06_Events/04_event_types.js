/*

Mouse Events:
click: Occurs when the user clicks on an element.
dblclick: Occurs when the user double-clicks on an element.
mousedown: Occurs when the user presses a mouse button over an element.
mouseup: Occurs when the user releases a mouse button over an element.
mouseover: Occurs when the pointer is moved onto an element.
mouseout: Occurs when the pointer is moved out of an element.
mousemove: Occurs when the pointer is moving within an element.
mouseenter: Occurs when the pointer is moved onto an element (does not bubble).
mouseleave: Occurs when the pointer is moved out of an element (does not bubble).
contextmenu: Occurs when the right mouse button is clicked (to open a context menu).

Keyboard Events:
keydown: Occurs when a key is pressed.
keypress: Occurs when a key is pressed and held.
keyup: Occurs when a key is released.

Form Events:
submit: Occurs when a form is submitted.
change: Occurs when the value of an element has been changed.
input: Occurs when the value of an input element is changed.
focus: Occurs when an element receives focus.
blur: Occurs when an element loses focus.
reset: Occurs when a form is reset.
select: Occurs when some text is selected in a text field.

Document/Window Events:
load: Occurs when an object has loaded.
unload: Occurs when the page is unloaded.
resize: Occurs when the window is resized.
scroll: Occurs when an element's scrollbar is being scrolled.
DOMContentLoaded: Occurs when the initial HTML document has been completely loaded and parsed.

Clipboard Events:
copy: Occurs when the user copies the content of an element.
cut: Occurs when the user cuts the content of an element.
paste: Occurs when the user pastes content into an element.

Drag Events:
drag: Occurs when an element is being dragged.
dragend: Occurs when the user has finished dragging an element.
dragenter: Occurs when the dragged element enters a drop target.
dragleave: Occurs when the dragged element leaves a drop target.
dragover: Occurs when the dragged element is being dragged over a drop target.
dragstart: Occurs when the user starts to drag an element.
drop: Occurs when the dragged element is dropped on the drop target.

Touch Events:
touchstart: Occurs when the user touches the screen.
touchmove: Occurs when the user moves the finger across the screen.
touchend: Occurs when the user removes the finger from the screen.
touchcancel: Occurs when the touch event is interrupted.
Focus Events
focusin: Occurs when an element is about to receive focus (bubbles).
focusout: Occurs when an element is about to lose focus (bubbles).

Animation Events:
animationstart: Occurs when a CSS animation has started.
animationend: Occurs when a CSS animation has completed.
animationiteration: Occurs when a CSS animation is repeated.
Transition Events
transitionstart: Occurs when a CSS transition has started.
transitionend: Occurs when a CSS transition has completed.
transitionrun: Occurs when a CSS transition is running.
transitioncancel: Occurs when a CSS transition is canceled.

Other Events:
abort: Occurs when the loading of a media is aborted.
error: Occurs when an error occurs while loading an external file.
wheel: Occurs when the mouse wheel is rolled up or down over an element.
pointerdown: Occurs when a pointer (pen or touch) is pressed down.
pointerup: Occurs when a pointer is released.
pointermove: Occurs when a pointer is moved.
pointerenter: Occurs when a pointer enters an element.
pointerleave: Occurs when a pointer leaves an element.
pointercancel: Occurs when the browser decides a pointer is no longer active. */